//Operacionales

let a = 10; 

console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//De igualdad

console.log(a == 10); // Pregunta solo por el valor
console.log(a != 10); 
console.log(a === '10'); //pregunta por el valor y el tipo
console.log(a !== '10');  // Distinto tipo de dato o de valor 
